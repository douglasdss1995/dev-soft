// Estrutura básica do loop for
for (inicialização; condição; incremento) {
  // código a ser executado
}

// Exemplo básico
for (let i = 0; i < 5; i++) {
  console.log(`Número: ${i}`);
}
// Output: 0, 1, 2, 3, 4

// Com tipagem específica
for (let contador: number = 1; contador <= 10; contador += 2) {
  console.log(`Número ímpar: ${contador}`);
}
// Output: 1, 3, 5, 7, 9

